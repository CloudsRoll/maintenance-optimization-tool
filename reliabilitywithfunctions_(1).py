import numpy as np

# Given the State Description Vector it returns a linear index number
def StateIndex(StateDescV, Dim, SetSize):
    #Function to linearize the state description
    Index = 0
    for i in range(Dim):
        Index += StateDescV[i]*((SetSize)**i)
    return int(Index)

# Given  a linear index number  it returns the State Description Vector
def StateDesc(Ind, Dim, SetSize):
    StateDescV = np.zeros(Dim,dtype=int)
    for i in range(Dim):
        StateDescV[i] = np.remainder(Ind,SetSize)
        Ind = np.floor_divide(Ind,SetSize)
    return StateDescV

def PreProcessing(alpha, K, C, Eps):
    DetSSize = K+1

    ProbReachingT = 1


    StateSpacesSize = DetSSize**C                               #Size of the state space for joint distribution
    TransitionM = np.zeros([StateSpacesSize,StateSpacesSize])
    DistributionV = np.zeros([1,StateSpacesSize])
    CondDistV = np.zeros(StateSpacesSize)
    ObsTransM= np.zeros([1,2])                                  #Transition Probabilities from Yellow States to Yellow or Red States at counter t(Row Number)

    NotRedMaskV = np.zeros(StateSpacesSize,dtype=int)
    NumberNonGreenV = np.zeros(StateSpacesSize,dtype=int)
    StateDescVector = np.zeros(C)

    #Begin Main Body

    # Create a list of Red State indices and a mask vector for non-red states
    RedList = []
    for Ind in range(StateSpacesSize):
        StateV = StateDesc(Ind,C,DetSSize)

        #Detect Red States
        RedState = 0
        for i in range(C):
            if StateV[i]==K:
                RedState = 1
                break

        if RedState==0:
            NotRedMaskV[Ind] = 1
        else:
            RedList.append(Ind)

        #Find Number of NonGreens

        NumNonGreen = 0
        for i in range(C):
            if StateV[i] !=0:
                NumNonGreen += 1

        NumberNonGreenV[Ind] = NumNonGreen



    #print("Number of States =",StateSpacesSize)
    #print("Not-Red Mask Vector =", NotRedMaskV)
    #print("Number of NonGreen Components Vector =", NumberNonGreenV)
    #print("List of Red States =", RedList)



    for FromInd in range(StateSpacesSize):
        FromStateV = StateDesc(FromInd,C,DetSSize)

        if NotRedMaskV[FromInd] == 0:
            TransitionM[FromInd,FromInd] =1         # Red States are modeled as absorbing
        else:
            for IncInd in range(2**C):
                IncV = StateDesc(IncInd,C,2)
                #print(IncInd,IncV)

                ToStateV = FromStateV+IncV
                ToStateInd = StateIndex(ToStateV,C,DetSSize)
                TransitionM[FromInd,ToStateInd]= (1-alpha)**(sum(IncV))*(alpha**(C-sum(IncV)))



    t=0
    DistributionV[t,:] = TransitionM[0,:]
    CondDistV = 1/(1-DistributionV[0,0])*DistributionV[t,:]
    CondDistV[0] = 0

    ObsTransM[0,:] = [1,0]

    DistributionV[t,:] = CondDistV


    #print(TransitionM)
    #print(DistributionV)


    while ProbReachingT > Eps:
    #for t in range(1,T):
        t += 1
        #DistributionV[t,:] = np.matmul(CondDistV,TransitionM)
        #print(DistributionV)
        #print(np.matmul(CondDistV,TransitionM))
        DistributionV = np.append(DistributionV, [np.matmul(CondDistV,TransitionM)], axis=0)
        #print(DistributionV)
        #print(DistributionV[t,:])
        CondDistV = np.multiply(DistributionV[t,:],NotRedMaskV)
        SumNotRed = np.sum(CondDistV)
        #ObsTransM[t,:] = [SumNotRed,1-SumNotRed]
        ObsTransM = np.append(ObsTransM, [[SumNotRed,1-SumNotRed]], axis=0)
        ProbReachingT *=  ObsTransM[t,0]
        CondDistV = 1/SumNotRed*CondDistV


    #print(DistributionV)
    #print(ObsTransM)
    #print('Prob', ProbReachingT)
    #print(ProbReachingT, t+1)

    return NumberNonGreenV, DistributionV, ObsTransM, t+1

