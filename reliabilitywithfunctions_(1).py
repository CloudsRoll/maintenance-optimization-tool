def ReliabilityLP(Code,K,C,U,alpha,cp,cc,cr,cu,co):
    DetSSize = K+1
    CounterStuckP = alpha**C
    ObsSTransProbMat = ObsTransM   #Rows are for the CounterState (SignalState is always NotRed); Colums are for the SignalState (CounterState is always increased by 1)
    StateSpacesSize = DetSSize**C                                 #Size of the state space for joint distribution

    PeriodCosts = np.zeros((2, U+1, C+1))
    PeriodCosts[0,:,1:C+1] = cp
    PeriodCosts[1,:,:] = cc

    for s in range(2):
        for n in range(0,U+1):     #   Check n
            for a in range(1, C+1):

                PeriodCosts[s,n,a] += ct*a

                if n == 0:
                  PeriodCosts[s,n,a] += (cr*NumberNonGreenV[0]+cu*max(NumberNonGreenV[0]-a,0)+co*max(-NumberNonGreenV[0]+a,0))

                else:
                  for Ind in range(StateSpacesSize):
                      PeriodCosts[s,n,a] += (cr*NumberNonGreenV[Ind]+cu*max(NumberNonGreenV[Ind]-a,0)+co*max(-NumberNonGreenV[Ind]+a,0))*DistributionV[n-1,Ind]




    #print(PeriodCosts)

    # Model
    lp = gp.Model("ReliabilityMDP")
    lp.setParam("FeasibilityTol", 1e-9)
    lp.setParam("NumericFocus", 2)

    #SignalSpace = range(2)                  # {NotRed, Red}
    #CounterSpace = range(U+1)               # {0,1, ... , U}
    DSpace = range(C+1)                     # {0,1, ... , C}

    P = lp.addMVar(shape=(2, U+1, C+1), name="P", obj=PeriodCosts)

    #Constraint 0  No invervention in state 0
    lp.addConstr(
        gp.quicksum(P[0,0,1:C+1])
        == 0)



    #Constraint 1
    lp.addConstr(
        gp.quicksum(P[0,0,:])
        -gp.quicksum(P[s,n,a]
            for s in range(2)
            for n in range(U+1)
            for a in range(1,C+1)
            )
        -CounterStuckP*P[0,0,0]
        == 0)

    #Constraint 2
    lp.addConstr(
        gp.quicksum(P[0,1,:])-
        (1-CounterStuckP)*P[0,0,0]
        == 0)

    #Constraint 3 and 4
    for n in range(2,U+1):
        lp.addConstr(
            gp.quicksum(P[0,n,:])-
            ObsSTransProbMat[n-1,0]*P[0,n-1,0]
            == 0)

        lp.addConstr(
            gp.quicksum(P[1,n,:])-
            ObsSTransProbMat[n-1,1]*P[0,n-1,0]
            == 0)

    #Constraint 5
    lp.addConstr(
        P.sum()
        == 1)

    #Constraint: In Red you have to intervene
    for n in range(U+1):
        lp.addConstr(
            P[1,n,0]
            == 0)

    #Constraint: You cannot reach Red State before the counter reaches K
    for n in range(K):
        for a in range(C+1):
            lp.addConstr(
                P[1,n,a]
                == 0)

    #Constraint: In Counter truncation point you have to intervene
    lp.addConstr(
        P[0,U,0]
        == 0)


    # If Code is 1 The Policy is intervene at K-1 taking optimal number of parts
    # If Code is 2 The Policy is intervene at K-1 taking only 1 part
    # If Code is 3 The Policy is intervene at K-1 taking C parts

    # If Code is 4 The Policy is intervene only at RED taking optimal number of parts
    # If Code is 5 The Policy is intervene only at RED taking only 1 part
    # If Code is 6 The Policy is intervene only at RED taking C parts



    if Code in [1,2,3]:
        for n in range(K-1):
            for a in range(1,C+1):
                lp.addConstr(
                    P[0,n,a]
                    == 0
                )
        lp.addConstr(
                P[0,K-1,0]
                ==0
        )

        if Code == 2:
            for a in range(2,C+1):
                lp.addConstr(
                    P[0,K-1,a]
                    ==0
                )
        elif Code == 3:
            for a in range(1,C):
                lp.addConstr(
                    P[0,K-1,a]
                    ==0
                )
    elif Code in [4,5,6]:
        for n in range(U):
            for a in range(1,C+1):
                lp.addConstr(
                    P[0,n,a]
                    ==0
                )
        if Code == 5:
            for n in range(U+1):
                for a in range(2,C+1):
                    lp.addConstr(
                        P[1,n,a]
                        ==0
                    )
        elif Code == 6:
            for n in range(U+1):
                for a in range(1,C):
                    lp.addConstr(
                        P[1,n,a]
                        ==0
                    )



    """
    lp.addConstr(P[0,0,1] == 0)
    lp.addConstr(P[0,0,2] == 0)
    """
    lp.optimize()
    SolutionMat = P.X
    Obj = lp.getObjective()
    ObjValue = Obj.getValue()
    #lp.display()
    return SolutionMat, ObjValue

